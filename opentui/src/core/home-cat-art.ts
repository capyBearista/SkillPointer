function normalizeCat(value: string): string {
  return value.replace(/\r\n/g, "\n").replace(/\n$/u, "");
}

function loadHomeCatFromFile(): string {
  const moduleDir = path.dirname(fileURLToPath(import.meta.url));
  const catPath = path.resolve(moduleDir, "../../../cat.txt");
  try {
    const fromFile = fs.readFileSync(catPath, "utf8");
    return normalizeCat(fromFile);
  } catch {
    return FALLBACK_HOME_CAT;
  }
}

export const HOME_CAT_ART = loadHomeCatFromFile();
